a = [1, 2, 3]

# append (push)
a.append(4)          # [1, 2, 3, 4]

# pop
a.pop()              # [1, 2, 3]

# insert (unshift)
a.insert(0, 0)       # [0, 1, 2, 3]

# pop index (shift)
a.pop(0)             # [1, 2, 3]

# remove by value
a.remove(2)          # [1, 3]

# slice
b = a[1:]             # [3]